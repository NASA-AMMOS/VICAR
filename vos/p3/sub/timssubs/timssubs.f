	SUBROUTINE CENTWAV(IDATE,WAVE)
C
C	Calculates the central wavelengths of the TIMS bands
C
C	IDATE input  integer    - date as yymmdd
C	WAVE  output real array - the 6 central wavelengths in microns
C
	REAL WAVE(6),FILTER(158,6),WVLN(158)
	COMMON /RAWFIL/ FILTER,WVLN,ICALDATE
C
        CALL GETFIL(IDATE,0)
	DO IBND=1,6
	   PSUM = 0.0
	   FSUM = 0.0
	   DO INDX=1,158
              PSUM = PSUM + (FILTER(INDX,IBND)*WVLN(INDX))
	      FSUM = FSUM + FILTER(INDX,IBND)
	   END DO
	   WAVE(IBND) = PSUM/FSUM
	END DO
	RETURN
	END
C******************************************************************************
	SUBROUTINE GETFIL(JDATE,BPADJ)
