C
C	VICAR program ASTERBTEMP, used to convert ASTER Level 1B (TIR) to
C	the ASTER Level 2 Brightness Temperature product.
C
C	Ron Alley	3 March 2004
C
	INCLUDE 'VICMAIN_FOR'
	SUBROUTINE MAIN44
C
	IMPLICIT NONE
	INTEGER IN,ISTAT,IOUT,ISL,ISS,NL,NS,NLIN,NSIN,ICHAN,ILINE,ISAMP
	INTEGER*2 IRAD(1000),ITEMP(1000)
	INTEGER*2 LUT(4095,5)
	COMMON /LUTVALS/ LUT
C						Open datasets, get size field
	CALL XVUNIT(IN,'INP',1,ISTAT,' ')
	CALL XVOPEN(IN,ISTAT,'IO_ACT','SA','OPEN_ACT','SA',
     +		    'U_FORMAT','HALF',' ')
	CALL XVUNIT(IOUT,'OUT',1,ISTAT,' ')
	CALL XVOPEN(IOUT,ISTAT,'IO_ACT','SA','OPEN_ACT','SA',
     +	           'OP','WRITE','U_FORMAT','HALF','O_FORMAT','HALF',
     +		   'U_NB',5,'U_NL',NL,'U_NS',NS,'U_ORG','BSQ',' ')
	CALL XVSIZE(ISL,ISS,NL,NS,NLIN,NSIN)
C							      update VICAR label
	CALL XLADD(IOUT,'HISTORY','LBL1',
     +		   'ASTER Brightness Temperature Image',ISTAT,
     +		   'FORMAT','STRING',' ')
	CALL XLADD(IOUT,'HISTORY','LBL2','DN = 100*Degrees Centigrade',
     +		   ISTAT,'FORMAT','STRING',' ')
C
C								   Do conversion
	DO ICHAN=1,5
	    DO ILINE=ISL,ISL+NL-1
		CALL XVREAD(IN,IRAD,ISTAT,'LINE',ILINE,'BAND',ICHAN,
     +			    'SAMP',ISS,'NSAMPS',NS,' ')
		DO ISAMP=1,NS
		    IF (IRAD(ISAMP) .LE. 0) THEN
			ITEMP(ISAMP) = -27315
		    ELSE IF (IRAD(ISAMP) .GT. 4095) THEN
			ITEMP(ISAMP) = 29999
		    ELSE
			ITEMP(ISAMP) = LUT(IRAD(ISAMP),ICHAN)
		    END IF
		END DO
		CALL XVWRIT(IOUT,ITEMP,ISTAT,' ')
	    END DO
	END DO
C								Close datasets
	CALL XVCLOSE(IN,ISTAT,' ')
	CALL XVCLOSE(IOUT,ISTAT,' ')
	RETURN
	END
